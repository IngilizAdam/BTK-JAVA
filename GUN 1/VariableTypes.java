public class VariableTypes {
    public static void main(String[] args) {
        int tamSayi = 0; // int tipi pozitif-negatif tam sayi depolamakta kullanilir
        double virgulluSayi = 1.5; // double tipi virgullu pozitif-negatif sayi depolamakta kullanilir
        char karakter = 'a'; // char tipi tek karakter depolamakta kullanilir
        String yazi = "abc"; // String tipi bir veya birden fazla karakterli yazilari depolamak icin kullanilir
        boolean mantiksalIfade = true; // boolean tipi mantiksal ifadeleri depolamak icin kullanilir

        // Bu tiplerin hepsi print ile yazilabilir
        System.out.println(tamSayi);
        System.out.println(virgulluSayi);
        System.out.println(karakter);
        System.out.println(yazi);
        System.out.println(mantiksalIfade);
    }
}
